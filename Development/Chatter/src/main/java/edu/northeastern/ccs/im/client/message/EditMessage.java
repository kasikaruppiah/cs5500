package edu.northeastern.ccs.im.client.message;

/**
 * Edit message is the client requesting the server to edit some parameter about the user.
 */
public class EditMessage extends Message {
  /**
   * Constructor to create the edit request.
   *
   * @param type      the enum type of the message
   * @param parameter the parameter to edit
   * @param newValue  new value of the parameter.
   */
  public EditMessage(MessageType type, String parameter, String newValue) {
    super(type, parameter, newValue);
  }
}
